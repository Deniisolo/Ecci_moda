function getStoredLang() {
  const saved = localStorage.getItem("moda-ecci-lang");
  return saved === "en" ? "en" : "es";
}

function t(lang, key) {
  return I18N[lang]?.[key] ?? I18N.es[key] ?? "";
}

function applyLanguage(lang) {
  const dict = I18N[lang] || I18N.es;

  document.documentElement.lang = lang;

  document.querySelectorAll("[data-i18n]").forEach((el) => {
    const key = el.dataset.i18n;
    if (dict[key] !== undefined) el.textContent = dict[key];
  });

  document.querySelectorAll("[data-i18n-html]").forEach((el) => {
    const key = el.dataset.i18nHtml;
    if (dict[key] !== undefined) el.innerHTML = dict[key];
  });

  document.querySelectorAll("[data-i18n-attr]").forEach((el) => {
    const pairs = el.dataset.i18nAttr.split(";");
    pairs.forEach((pair) => {
      const [attr, key] = pair.split(":").map((s) => s.trim());
      if (dict[key] !== undefined) el.setAttribute(attr, dict[key]);
    });
  });

  const marqueeSpans = document.querySelectorAll(".marquee-track span");
  const half = marqueeSpans.length / 2;
  marqueeSpans.forEach((span, i) => {
    const key = `marquee.${i % half}`;
    if (dict[key] !== undefined) span.textContent = dict[key];
  });

  document.title = dict["meta.title"];
  const metaDesc = document.querySelector('meta[name="description"]');
  if (metaDesc) metaDesc.setAttribute("content", dict["meta.description"]);

  document.querySelectorAll(".lang-btn").forEach((btn) => {
    btn.classList.toggle("is-active", btn.dataset.lang === lang);
    btn.setAttribute("aria-pressed", btn.dataset.lang === lang ? "true" : "false");
  });

  localStorage.setItem("moda-ecci-lang", lang);
  document.dispatchEvent(new CustomEvent("languagechange", { detail: { lang } }));
}

document.addEventListener("DOMContentLoaded", () => {
  const switcher = document.querySelector(".lang-switch");
  if (!switcher) return;

  switcher.querySelectorAll(".lang-btn").forEach((btn) => {
    btn.addEventListener("click", () => {
      if (btn.dataset.lang !== getStoredLang()) applyLanguage(btn.dataset.lang);
    });
  });

  applyLanguage(getStoredLang());
});

window.ModaECCI = { applyLanguage, getLang: getStoredLang, t };
